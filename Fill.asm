// Restart>>key-check>>black or white>>PAINT>>RESTART

(RESTART)
@SCREEN 
D=A
@0
M=D //STORING SCREEN'S ADDRESS IN R0

(KBDCHECK)
@KBD
D=M // CHECK KEY VALUE >> IDLE=0 >> PRESSED = GREATER THAN 0

@WHITE
D;JEQ //IF 0 JMP TPO WHITE

@BLACK
D;JGT // OTHERWISE IF KEY IS PRESSED JMP TO BLACK

@KDBCHECK
0;JMP //LOOP TO CHECK KEYS AGAIN AND AGAIN

(WHITE)
@1
M=0
@PAINT
0;JMP	//JUMP TO PAINT SCREEN WHITE

(BLACK)
@1
M=-1
@PAINT
0;JMP //JMP TO PAINT SCREEN BLACK

(PAINT)
@1
D=M

@0
A=M
M=D

@0
D=M+1
@KBD
D=A-D

@0
M=M+1
A=M

@PAINT
D;JGT //REPEAT LOOP UNTIL D IS 0

@RESTART
0;JMP //LOOP WHOLE WHEN SCREEN IS TOTAL BLACK OR WHITE
